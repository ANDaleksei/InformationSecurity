import Foundation

func testKalynaConfig1Encryption() {
  let kalyna = KalynaAlgorithm(
    key: Data([0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F]),
    config: .config1
  )
  let encruptedData = kalyna.encode(data: Data([0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F]))
  assert(encruptedData == Data([0x81, 0xBF, 0x1C, 0x7D, 0x77, 0x9B, 0xAC, 0x20, 0xE1, 0xC9, 0xEA, 0x39, 0xB4, 0xD2, 0xAD, 0x06]))
}

func testKalynaConfig1Decryption() {
